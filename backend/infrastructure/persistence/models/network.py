"""M38 Supplier Network Intelligence — ORM Models.

Tables:
  supplier_relationships      — directed edges between suppliers
  suggested_relationships     — pending human-approval relationship proposals
  network_exposure_signals    — indirect risk exposure propagation records
  supplier_criticality        — computed criticality classification per supplier
  dependency_analyses         — org and per-supplier dependency metrics
  resilience_assessments      — resilience scoring per org / supplier
  incident_clusters           — grouped findings/signals sharing a root cause
"""

from __future__ import annotations

from datetime import datetime

from sqlalchemy import DateTime, Float, Index, Integer, String, Text, UniqueConstraint
from sqlalchemy.dialects.postgresql import JSONB
from sqlalchemy.orm import Mapped, mapped_column
from sqlalchemy import text

from .base import BaseModel


class SupplierRelationshipModel(BaseModel):
    """Directed relationship edge between two suppliers within an org.

    Explainability:
      confidence   — 0.0–1.0 from discovery engine or manual override
      source       — MANUAL | DISCOVERY | IMPORT
      rationale    — human-readable reason stored immutably at creation time
    """

    __tablename__ = "supplier_relationships"
    __table_args__ = (
        Index("ix_rel_org", "organization_id"),
        Index("ix_rel_supplier", "supplier_id"),
        Index("ix_rel_related", "related_supplier_id"),
        Index("ix_rel_type", "relationship_type"),
        Index("ix_rel_status", "relationship_status"),
    )

    organization_id: Mapped[str] = mapped_column(String(36), nullable=False)
    supplier_id: Mapped[str] = mapped_column(String(36), nullable=False)
    related_supplier_id: Mapped[str] = mapped_column(String(36), nullable=False)
    # PARENT_COMPANY SUBSIDIARY SISTER_COMPANY SHARED_COUNTRY SHARED_SECTOR
    # SHARED_SUPPLY_CHAIN SHARED_INCIDENT SHARED_LOGISTICS
    # SHARED_REGULATORY_EXPOSURE CUSTOM
    relationship_type: Mapped[str] = mapped_column(String(50), nullable=False)
    confidence: Mapped[float] = mapped_column(Float, nullable=False, default=1.0)
    source: Mapped[str] = mapped_column(String(30), nullable=False, default="MANUAL")
    rationale: Mapped[str] = mapped_column(Text, nullable=False, default="")
    relationship_status: Mapped[str] = mapped_column(
        String(20), nullable=False, default="ACTIVE"
    )
    removed_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True), nullable=True
    )
    removed_by: Mapped[str | None] = mapped_column(String(36), nullable=True)
    calculation_inputs: Mapped[dict] = mapped_column(JSONB, nullable=False, default=dict)


class SuggestedRelationshipModel(BaseModel):
    """Proposed relationship awaiting human approval.

    Generated by RelationshipDiscoveryEngine. Must be APPROVED before a
    SupplierRelationshipModel row is created. Rejected proposals are retained
    for the audit trail.
    """

    __tablename__ = "suggested_relationships"
    __table_args__ = (
        Index("ix_sugg_org", "organization_id"),
        Index("ix_sugg_supplier", "supplier_id"),
        Index("ix_sugg_status", "suggestion_status"),
    )

    organization_id: Mapped[str] = mapped_column(String(36), nullable=False)
    supplier_id: Mapped[str] = mapped_column(String(36), nullable=False)
    related_supplier_id: Mapped[str] = mapped_column(String(36), nullable=False)
    relationship_type: Mapped[str] = mapped_column(String(50), nullable=False)
    confidence: Mapped[float] = mapped_column(Float, nullable=False)
    rationale: Mapped[str] = mapped_column(Text, nullable=False, default="")
    suggestion_source: Mapped[str] = mapped_column(String(50), nullable=False)
    # PENDING | APPROVED | REJECTED
    suggestion_status: Mapped[str] = mapped_column(
        String(20), nullable=False, default="PENDING"
    )
    reviewed_by: Mapped[str | None] = mapped_column(String(36), nullable=True)
    reviewed_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True), nullable=True
    )
    review_note: Mapped[str | None] = mapped_column(Text, nullable=True)
    calculation_inputs: Mapped[dict] = mapped_column(JSONB, nullable=False, default=dict)


class NetworkExposureSignalModel(BaseModel):
    """Indirect risk exposure record.

    Captures propagated risk from an origin supplier to an impacted supplier
    through a relationship path. Severity and confidence attenuate by distance.
    """

    __tablename__ = "network_exposure_signals"
    __table_args__ = (
        Index("ix_nexp_org", "organization_id"),
        Index("ix_nexp_origin", "origin_supplier_id"),
        Index("ix_nexp_impacted", "impacted_supplier_id"),
        Index("ix_nexp_status", "exposure_status"),
        Index("ix_nexp_type", "exposure_type"),
        # P0 M38.1: prevent duplicate ACTIVE signals for same (org, origin, impacted, type)
        Index(
            "uq_nexp_active_signal",
            "organization_id",
            "origin_supplier_id",
            "impacted_supplier_id",
            "exposure_type",
            unique=True,
            postgresql_where=text("exposure_status = 'ACTIVE'"),
        ),
    )

    organization_id: Mapped[str] = mapped_column(String(36), nullable=False)
    origin_supplier_id: Mapped[str] = mapped_column(String(36), nullable=False)
    impacted_supplier_id: Mapped[str] = mapped_column(String(36), nullable=False)
    # SANCTIONS | HIGH_RISK_COUNTRY | REGULATORY | HUMAN_RIGHTS | CASCADE | CUSTOM
    exposure_type: Mapped[str] = mapped_column(String(50), nullable=False)
    propagation_path: Mapped[list] = mapped_column(JSONB, nullable=False, default=list)
    path_length: Mapped[int] = mapped_column(Integer, nullable=False, default=1)
    confidence: Mapped[float] = mapped_column(Float, nullable=False)
    severity: Mapped[str] = mapped_column(String(20), nullable=False, default="MEDIUM")
    rationale: Mapped[str] = mapped_column(Text, nullable=False, default="")
    source_signal_id: Mapped[str | None] = mapped_column(String(36), nullable=True)
    source_finding_id: Mapped[str | None] = mapped_column(String(36), nullable=True)
    # ACTIVE | RESOLVED | DISMISSED
    exposure_status: Mapped[str] = mapped_column(
        String(20), nullable=False, default="ACTIVE"
    )
    detected_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), nullable=False
    )
    resolved_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True), nullable=True
    )
    calculation_inputs: Mapped[dict] = mapped_column(JSONB, nullable=False, default=dict)



class NetworkWatchlistEntryModel(BaseModel):
    """Network expansion of a supplier watchlist entry.

    When a supplier is added to SupplierWatchlistModel, all suppliers in its
    BFS neighborhood (depth ≤ 2) are recorded here so that HIGH/CRITICAL
    signals on related suppliers surface as watchlist alerts.
    """

    __tablename__ = "network_watchlist_entries"
    __table_args__ = (
        Index("ix_nwl_org", "organization_id"),
        Index("ix_nwl_watched", "watched_supplier_id"),
        Index("ix_nwl_related", "related_supplier_id"),
        # Each related supplier appears only once per watched supplier
        UniqueConstraint(
            "organization_id",
            "watched_supplier_id",
            "related_supplier_id",
            name="uq_nwl_pair",
        ),
    )

    organization_id: Mapped[str] = mapped_column(String(36), nullable=False)
    watched_supplier_id: Mapped[str] = mapped_column(String(36), nullable=False)
    related_supplier_id: Mapped[str] = mapped_column(String(36), nullable=False)
    distance: Mapped[int] = mapped_column(Integer, nullable=False, default=1)
